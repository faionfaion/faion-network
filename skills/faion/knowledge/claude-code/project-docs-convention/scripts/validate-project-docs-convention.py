#!/usr/bin/env python3
"""validate-project-docs-convention.py

Validate the spec artefact for the project-docs-convention methodology against the schema in
02-output-contract.xml.

Inputs:
    --file PATH       path to artefact JSON
    --self-test       run built-in fixtures
    --help            this message

Exit codes:
    0 = valid
    1 = invalid
    2 = usage / unreadable
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REQUIRED = ['repo_root', 'dirs', 'violations']


def validate(obj: dict) -> list[str]:
    errs: list[str] = []
    if not isinstance(obj, dict):
        return ["root must be object"]
    for k in REQUIRED:
        if k not in obj:
            errs.append(f"missing required field: {k}")
    return errs


OK = {'repo_root': '/home/nero/workspace/projects/faion-net/faion-network', 'audited_at': '2026-05-22', 'dirs': [{'path': 'skills/faion/knowledge/free/dev/code-quality', 'has_claude_md': True, 'has_agents_md': True, 'agents_md_lines': 42, 'verdict': 'ok'}, {'path': 'skills/faion/knowledge/free/dev/legacy-prototype', 'has_claude_md': False, 'has_agents_md': False, 'agents_md_lines': 0, 'verdict': 'create'}], 'violations': []}
BAD = {'dirs': []}


def self_test() -> int:
    if validate(OK):
        sys.stderr.write("ok rejected\n"); return 1
    if not validate(BAD):
        sys.stderr.write("bad accepted\n"); return 1
    sys.stdout.write("self-test OK\n")
    return 0


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--file", type=str)
    ap.add_argument("--self-test", action="store_true")
    args = ap.parse_args()
    if args.self_test:
        return self_test()
    if not args.file:
        ap.print_help()
        return 2
    p = Path(args.file)
    if not p.is_file():
        sys.stderr.write(f"not a file: {p}\n"); return 2
    obj = json.loads(p.read_text())
    errs = validate(obj)
    if errs:
        for e in errs:
            sys.stderr.write(f"VIOLATION: {e}\n")
        return 1
    sys.stdout.write("OK\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
