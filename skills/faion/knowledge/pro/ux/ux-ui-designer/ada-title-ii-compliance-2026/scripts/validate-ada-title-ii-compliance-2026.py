#!/usr/bin/env python3
"""validate-ada-title-ii-compliance-2026.py

Validate ADA Title II Compliance 2026 artefact JSON against the schema in 02-output-contract.xml.

Inputs:
    --file PATH       path to artefact JSON
    --self-test       run built-in fixtures
    --help            this message

Exit codes:
    0 = valid
    1 = invalid (violations printed to stderr)
    2 = usage / unreadable input
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REQUIRED = ['entity_name', 'population_bucket', 'deadline', 'wcag_target', 'scope_urls', 'audit_evidence', 'gaps']


def validate(obj) -> list:
    errs: list = []
    if not isinstance(obj, dict):
        return ["root must be object"]
    for k in REQUIRED:
        if k not in obj or obj[k] in (None, "", []):
            errs.append(f"missing or empty required field: {k}")
    return errs


OK = {'entity_name': 'City of Example (75,000 pop)', 'population_bucket': '>=50k', 'deadline': '2026-04-24', 'wcag_target': '2.1-AA', 'scope_urls': ['https://example.gov/', 'https://services.example.gov/permits'], 'audit_evidence': {'audit_date': '2026-03-15', 'findings_count': 12}, 'gaps': [{'id': 'G-001', 'sc': '1.4.3', 'remediation': 'Recolour brand secondary to pass 4.5:1'}]}
BAD = {'entity_name': 'X', 'wcag_target': '2.2-AA'}


def self_test() -> int:
    if validate(OK):
        sys.stderr.write("ok rejected\n")
        return 1
    if not validate(BAD):
        sys.stderr.write("bad accepted\n")
        return 1
    sys.stdout.write("self-test OK\n")
    return 0


def main() -> int:
    ap = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    ap.add_argument("--file", type=str)
    ap.add_argument("--self-test", action="store_true")
    args = ap.parse_args()
    if args.self_test:
        return self_test()
    if not args.file:
        ap.print_help()
        return 2
    p = Path(args.file)
    if not p.is_file():
        sys.stderr.write(f"not a file: {p}\n")
        return 2
    try:
        obj = json.loads(p.read_text())
    except json.JSONDecodeError as e:
        sys.stderr.write(f"invalid JSON: {e}\n")
        return 2
    errs = validate(obj)
    if errs:
        for e in errs:
            sys.stderr.write(f"VIOLATION: {e}\n")
        return 1
    sys.stdout.write("OK\n")
    return 0


if __name__ == "__main__":
    sys.exit(main())
